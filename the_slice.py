#The Players
players = ["Charlie", "Jason", "Derick", "Paul", "Simon"]

#Slicing from the beginning of a list
print(players[0:3])

# Another way to slice from the beginning of a list
print(players[:4])

#Slicing the middle of a list
print(players[2:4])

#If you want to slice to the end of a list
print(players[-4:])
